public class sorteio {
    int id;
    int ano;
    int indentificador;
    int mes;
    int valor_premio;
    int apresentador;
    int auditor;
    int premiado;
    String descriçao;
    String tipo;

    public sorteio() {
        this.ano=ano;
        this.indentificador=indentificador;
        this.mes=mes;
        this.valor_premio=valor_premio;
        this.apresentador=apresentador;
        this.auditor=auditor;
        this.premiado=premiado;
        this.id=id;
        this.tipo=tipo;
        this.descriçao=descriçao;
    }
}
